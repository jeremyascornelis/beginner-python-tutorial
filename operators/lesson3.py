#Comparison Operators
"""
1. ==
2. !=
3. >
4. >=
5. <
6. <=
"""

number1 = 3
number2 = 5
number3 = 3

print(number1 == number3) #3 == 3 -> True
print(number1 != number3) # 3 != 3 -> False
print(number2 > number3) # 5 > 3 -> True
print(number1 >= number3) #3 >= 3 -> True
print(number1 < number2) #3 < 5 -> True
print(number1 <= number3) #3 <= 3 -> True
print(number3 <= number2) #3 <= 5 -> True