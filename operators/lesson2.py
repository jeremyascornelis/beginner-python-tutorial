#asssignment operators = assigning a value
"""
1. = 
2. +=
3. -=
4. *=
5. /=
6. %=
7. **=
8. //=

"""

var1 = 10

var1 += 3
print(var1) #13

var1 -= 3
print(var1) #10

var1 *= 3
print(var1) #30

var1 /= 3
print(var1) #10.0

var1 %= 3
print(var1) #1.0

var1 **= 3
print(var1) #1.0

var1 //= 3
print(var1) #0.0
